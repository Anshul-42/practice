
public class insquareshape 
{
	    public static void main(String[] args)
	    {

	        // Forward
	        int i = 1;
	        while (i <= 100)
	        {
	            System.out.println(i + " square = " + (i * i));
	            i++;
	        }

	        System.out.println("---- Reverse ----");

	        // Backward
	        i = 100;
	        while (i >= 1)
	        {
	            System.out.println(i + " square = " + (i * i));
	            i--;
	        }

	    }
}
